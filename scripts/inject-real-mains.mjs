#!/usr/bin/env node
// scripts/inject-real-mains.mjs
//
// Genera el bloque `export const realMains = { ... }` para
// src/utils/testHarnesses.js a partir de los main.c presentes en rank02.
// El bloque se imprime en stdout — el desarrollador lo pega al final del
// archivo (idempotencia manual).
//
// Cada entrada lleva:
//   {
//     main: '<contenido>',
//     requiereWarningsRelajados: <bool>,  // true si el main no pasa -Werror
//   }
//
// Detectamos requiereWarningsRelajados con una heurística simple:
//   - usa %u en printf para un argumento que probablemente es int
//   - llama a free sobre puntero local sin malloc previo
//   - declara variables sin usar (-Wunused)

import { readFileSync, readdirSync, existsSync } from 'node:fs'
import { join, dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const REPO_ROOT = resolve(__dirname, '..')

const LEVEL_MAP = { level0: 1, level1: 2, level2: 3, level3: 4 }

function escapeTemplate(s) {
  return s
    .replace(/\\/g, '\\\\')
    .replace(/`/g, '\\`')
    .replace(/\$\{/g, '\\${')
}

function inferRelaxedFlag(mainText) {
  // %u con cosa que parece int (no unsigned)
  if (/printf\([^)]*%u[^)]*\*[a-zA-Z_]\w*[^)]*\)/.test(mainText)) return true
  if (/%u[^"]*"[^)]*,\s*\*?[a-zA-Z_]\w*/.test(mainText) &&
      !/unsigned/i.test(mainText)) {
    return true
  }
  return false
}

const entries = []

for (const rankLevel of Object.keys(LEVEL_MAP)) {
  const dir = join(REPO_ROOT, 'rank02', rankLevel)
  if (!existsSync(dir)) continue
  for (const id of readdirSync(dir).sort()) {
    const mainPath = join(dir, id, 'main.c')
    if (!existsSync(mainPath)) continue
    const text = readFileSync(mainPath, 'utf8').replace(/\r\n/g, '\n').trimEnd()
    entries.push({
      id,
      relaxed: inferRelaxedFlag(text),
      text,
    })
  }
}

console.log('// AUTOGEN — pegar al final de src/utils/testHarnesses.js')
console.log('// Generated by scripts/inject-real-mains.mjs')
console.log('//')
console.log('// Mains reales del repo rank02. Disponibles para los harnesses de')
console.log('// tipoEntrega="funcion" cuando se quiere ejecutar EXACTAMENTE el')
console.log('// main de referencia en vez del de la plataforma.')
console.log('//')
console.log('// Algunos mains no compilan con -Wall -Wextra -Werror (warnings reales')
console.log('// en el código del repo). Se marcan con requiereWarningsRelajados:true')
console.log('// para que el llamador pase -w al compilador.')
console.log('')
console.log('export const realMains = {')
for (const e of entries) {
  console.log(`  ${e.id}: {`)
  console.log(`    requiereWarningsRelajados: ${e.relaxed},`)
  console.log(`    main: \`${escapeTemplate(e.text)}\`,`)
  console.log(`  },`)
}
console.log('}')
console.log('')
console.log('/**')
console.log(' * Variante de buildFullCode que permite usar el main real del rank02.')
console.log(' * Devuelve { code, requiereWarningsRelajados } para que el caller')
console.log(' * pueda ajustar las flags de gcc.')
console.log(' */')
console.log('export function buildFullCodeWithRealMain(exerciseId, tipoEntrega, userCode) {')
console.log('  if (tipoEntrega !== "funcion") {')
console.log('    return { code: userCode, requiereWarningsRelajados: false }')
console.log('  }')
console.log('  const m = realMains[exerciseId]')
console.log('  const h = testHarnesses[exerciseId]')
console.log('  if (!m) {')
console.log('    // sin main real disponible — caemos al harness normal')
console.log('    const fallback = h ? h.header + userCode + "\\n" + h.main : userCode')
console.log('    return { code: fallback, requiereWarningsRelajados: false }')
console.log('  }')
console.log('  const header = h ? h.header : ""')
console.log('  return {')
console.log('    code: header + userCode + "\\n" + m.main,')
console.log('    requiereWarningsRelajados: m.requiereWarningsRelajados,')
console.log('  }')
console.log('}')
